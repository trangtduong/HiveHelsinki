int	ft_any(char **tab, int (*f)(char*))
{
	int i;

	if (!tab)
		return (0);
	i = -1;
	while (tab[++i] != 0)
	{
		if (f(tab[i]) == 1)
			return (1);
	}
	return (0);
}
