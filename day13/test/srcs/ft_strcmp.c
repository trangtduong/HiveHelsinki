int	cmp(void *str1, void *str2)
{
	char *s1;
	char *s2;

	s1 = (char *)str1;
	s2 = (char *)str2;
	while (*s1 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
